export const AgeCalculator: React.FC = () => {
  const [dob, setDob] = useState('');
  const [age, setAge] = useState({ y: 0, m: 0, d: 0 });

  const calculate = () => {
    if (!dob) return;
    const birth = new Date(dob);
    const now = new Date();
    let y = now.getFullYear() - birth.getFullYear();
    let m = now.getMonth() - birth.getMonth();
    let d = now.getDate() - birth.getDate();
    if (d < 0) { m--; d += 30; }
    if (m < 0) { y--; m += 12; }
    setAge({ y, m, d });
  };

  return (
    <div className="space-y-6 max-w-md mx-auto">
      <div><label className="block text-sm font-medium mb-1">Date of Birth</label><input type="date" value={dob} onChange={e=>setDob(e.target.value)} className="w-full p-3 border rounded-lg"/></div>
      <button onClick={calculate} className="w-full bg-brand-600 text-white py-2 rounded-lg hover:bg-brand-700">Calculate Age</button>
      {age.y > 0 && (
        <div className="flex justify-between text-center bg-slate-100 p-4 rounded-lg">
          <div><div className="text-2xl font-bold">{age.y}</div><div className="text-xs uppercase text-slate-500">Years</div></div>
          <div><div className="text-2xl font-bold">{age.m}</div><div className="text-xs uppercase text-slate-500">Months</div></div>
          <div><div className="text-2xl font-bold">{age.d}</div><div className="text-xs uppercase text-slate-500">Days</div></div>
        </div>
      )}
    </div>
  );
};

export const DateDiffCalculator: React.FC = () => {
    const [start, setStart] = useState('');
    const [end, setEnd] = useState('');
    const [diff, setDiff] = useState<number | null>(null);
    
    const calc = () => {
        if(start && end) {
            const d1 = new Date(start);
            const d2 = new Date(end);
            setDiff(Math.abs(d2.getTime() - d1.getTime()));
        }
    }

    return (
        <div className="grid md:grid-cols-2 gap-4">
            <div><label className="block text-sm mb-1">Start Date</label><input type="date" value={start} onChange={e=>setStart(e.target.value)} className="w-full p-2 border rounded"/></div>
            <div><label className="block text-sm mb-1">End Date</label><input type="date" value={end} onChange={e=>setEnd(e.target.value)} className="w-full p-2 border rounded"/></div>
            <button onClick={calc} className="md:col-span-2 bg-brand-600 text-white py-2 rounded">Calculate Difference</button>
            {diff !== null && (
                <div className="md:col-span-2 bg-slate-50 p-4 rounded text-center">
                    <p className="font-bold text-xl">{Math.floor(diff / (1000 * 60 * 60 * 24))} Days</p>
                    <p className="text-sm text-slate-500">or {Math.floor(diff / (1000 * 60 * 60))} Hours</p>
                </div>
            )}
        </div>
    )
}

