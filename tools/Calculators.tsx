// --- HEALTH ---
export const BMICalculator: React.FC = () => {
  const [unit, setUnit] = useState<'metric' | 'imperial'>('metric');
  
  // Inputs
  const [weight, setWeight] = useState<string>(''); 
  const [height, setHeight] = useState<string>(''); // cm
  const [heightFt, setHeightFt] = useState<string>('');
  const [heightIn, setHeightIn] = useState<string>('');
  
  // Results
  const [bmi, setBmi] = useState<number | null>(null);
  const [category, setCategory] = useState<string>('');
  const [resultColor, setResultColor] = useState<string>('bg-slate-50');
  const [error, setError] = useState<string>('');
  const [copied, setCopied] = useState(false);

  const calculate = () => {
    setError('');
    setBmi(null);

    const w = parseFloat(weight);
    if (isNaN(w) || w <= 0) {
        setError('Please enter a valid positive weight.');
        return;
    }

    let calculatedBMI = 0;

    if (unit === 'metric') {
        const hCm = parseFloat(height);
        if (isNaN(hCm) || hCm <= 0) {
            setError('Please enter a valid positive height.');
            return;
        }
        const hM = hCm / 100; // convert to meters
        calculatedBMI = w / (hM * hM);
    } else {
        const ft = parseFloat(heightFt) || 0;
        const inch = parseFloat(heightIn) || 0;
        if ((ft === 0 && inch === 0) || ft < 0 || inch < 0) {
            setError('Please enter a valid positive height.');
            return;
        }
        const totalInches = (ft * 12) + inch;
        // Imperial Formula: 703 x weight (lbs) / [height (in)]^2
        calculatedBMI = (w / (totalInches * totalInches)) * 703;
    }

    setBmi(calculatedBMI);

    // Determine Category and Color
    if (calculatedBMI < 18.5) {
        setCategory('Underweight');
        setResultColor('bg-blue-50 text-blue-700 border-blue-200');
    } else if (calculatedBMI < 25) {
        setCategory('Normal Weight');
        setResultColor('bg-green-50 text-green-700 border-green-200');
    } else if (calculatedBMI < 30) {
        setCategory('Overweight');
        setResultColor('bg-orange-50 text-orange-700 border-orange-200');
    } else {
        setCategory('Obese');
        setResultColor('bg-red-50 text-red-700 border-red-200');
    }
  };

  const reset = () => {
    setWeight('');
    setHeight('');
    setHeightFt('');
    setHeightIn('');
    setBmi(null);
    setError('');
    setResultColor('bg-slate-50');
    setCategory('');
  };

  const handleDownload = () => {
      if (!bmi) return;
      const hDisplay = unit === 'metric' ? `${height} cm` : `${heightFt || 0}' ${heightIn || 0}"`;
      const wDisplay = `${weight} ${unit === 'metric' ? 'kg' : 'lbs'}`;
      const text = `BMI Report\n==========\nWeight: ${wDisplay}\nHeight: ${hDisplay}\n\nBMI Result: ${bmi.toFixed(2)}\nCategory: ${category}\n\nGenerated by ToolMaster Pro`;
      
      const blob = new Blob([text], { type: 'text/plain' });
      const url = URL.createObjectURL(blob);
      const a = document.createElement('a');
      a.href = url;
      a.download = 'bmi-report.txt';
      a.click();
      URL.revokeObjectURL(url);
  };

  return (
    <div className="grid md:grid-cols-2 gap-8">
      {/* Inputs Panel */}
      <div className="space-y-6">
         {/* Unit Selector */}
         <div className="flex bg-slate-100 p-1 rounded-lg">
            <button 
                onClick={() => {setUnit('metric'); reset()}} 
                className={`flex-1 py-2 text-sm font-medium rounded-md transition-all ${unit === 'metric' ? 'bg-white text-brand-600 shadow-sm ring-1 ring-slate-200' : 'text-slate-500 hover:text-slate-700'}`}
            >
                Metric (kg / cm)
            </button>
            <button 
                onClick={() => {setUnit('imperial'); reset()}} 
                className={`flex-1 py-2 text-sm font-medium rounded-md transition-all ${unit === 'imperial' ? 'bg-white text-brand-600 shadow-sm ring-1 ring-slate-200' : 'text-slate-500 hover:text-slate-700'}`}
            >
                Imperial (lbs / ft)
            </button>
         </div>

         {/* Weight Input */}
         <div>
            <label className="block text-sm font-medium text-slate-700 mb-1">Weight ({unit === 'metric' ? 'kg' : 'lbs'})</label>
            <input 
                type="number" 
                value={weight}
                onChange={(e) => setWeight(e.target.value)}
                className="w-full p-3 border border-slate-300 rounded-lg focus:ring-2 focus:ring-brand-500 outline-none transition placeholder:text-slate-300"
                placeholder={unit === 'metric' ? 'e.g. 70' : 'e.g. 154'}
                min="0"
            />
         </div>

         {/* Height Input */}
         <div>
            <label className="block text-sm font-medium text-slate-700 mb-1">Height</label>
            {unit === 'metric' ? (
                <div className="relative">
                    <input 
                        type="number" 
                        value={height}
                        onChange={(e) => setHeight(e.target.value)}
                        className="w-full p-3 border border-slate-300 rounded-lg focus:ring-2 focus:ring-brand-500 outline-none transition placeholder:text-slate-300"
                        placeholder="e.g. 175"
                        min="0"
                    />
                    <span className="absolute right-3 top-1/2 -translate-y-1/2 text-slate-400 text-sm font-medium bg-white px-1">cm</span>
                </div>
            ) : (
                <div className="flex gap-4">
                    <div className="relative flex-1">
                        <input 
                            type="number" 
                            value={heightFt}
                            onChange={(e) => setHeightFt(e.target.value)}
                            className="w-full p-3 border border-slate-300 rounded-lg focus:ring-2 focus:ring-brand-500 outline-none placeholder:text-slate-300"
                            placeholder="5"
                            min="0"
                        />
                        <span className="absolute right-3 top-1/2 -translate-y-1/2 text-slate-400 text-sm font-medium bg-white px-1">ft</span>
                    </div>
                    <div className="relative flex-1">
                        <input 
                            type="number" 
                            value={heightIn}
                            onChange={(e) => setHeightIn(e.target.value)}
                            className="w-full p-3 border border-slate-300 rounded-lg focus:ring-2 focus:ring-brand-500 outline-none placeholder:text-slate-300"
                            placeholder="9"
                            min="0"
                        />
                        <span className="absolute right-3 top-1/2 -translate-y-1/2 text-slate-400 text-sm font-medium bg-white px-1">in</span>
                    </div>
                </div>
            )}
         </div>

         {/* Error Message */}
         {error && (
            <div className="flex items-center gap-2 text-red-600 text-sm bg-red-50 p-3 rounded-lg border border-red-100 animate-in fade-in slide-in-from-top-1">
                <AlertCircle className="w-4 h-4 shrink-0" /> {error}
            </div>
         )}

         {/* Action Buttons */}
         <div className="flex gap-3 pt-2">
            <button 
                onClick={calculate} 
                className="flex-1 bg-brand-600 text-white py-3 rounded-lg font-bold hover:bg-brand-700 transition-all flex items-center justify-center gap-2 shadow-lg hover:shadow-brand-200 active:scale-[0.98]"
            >
                <Calculator className="w-4 h-4" /> Calculate BMI
            </button>
            <button 
                onClick={reset} 
                className="px-4 bg-slate-100 text-slate-600 rounded-lg hover:bg-slate-200 transition hover:text-slate-800 border border-slate-200" 
                title="Reset Inputs"
            >
                <RefreshCw className="w-5 h-5" />
            </button>
         </div>
      </div>

      {/* Results Panel */}
      <div className={`rounded-xl p-8 border flex flex-col items-center justify-center text-center transition-all duration-500 ${bmi ? resultColor : 'bg-slate-50 border-slate-200'}`}>
          {bmi ? (
            <div className="animate-in fade-in zoom-in duration-300 w-full">
                <h3 className="text-lg font-medium opacity-80 mb-4">Your BMI Score</h3>
                <div className="text-6xl font-extrabold tracking-tight mb-4">{bmi.toFixed(1)}</div>
                <div className="inline-block px-4 py-1.5 rounded-full text-sm font-bold uppercase tracking-wide bg-white/60 border border-black/5 shadow-sm mb-8">
                    {category}
                </div>
                
                <div className="flex gap-3 justify-center border-t border-black/5 pt-6 mt-2">
                    <button 
                        onClick={() => {
                            navigator.clipboard.writeText(`BMI: ${bmi.toFixed(1)} - ${category}`);
                            setCopied(true);
                            setTimeout(() => setCopied(false), 2000);
                        }}
                        className="flex items-center gap-2 text-sm font-medium opacity-70 hover:opacity-100 transition bg-white/50 px-3 py-1.5 rounded hover:bg-white"
                    >
                        {copied ? <Check className="w-4 h-4" /> : <Copy className="w-4 h-4" />} {copied ? 'Copied' : 'Copy'}
                    </button>
                    <button 
                        onClick={handleDownload} 
                        className="flex items-center gap-2 text-sm font-medium opacity-70 hover:opacity-100 transition bg-white/50 px-3 py-1.5 rounded hover:bg-white"
                    >
                        <Download className="w-4 h-4" /> Save Report
                    </button>
                </div>
            </div>
          ) : (
            <div className="text-slate-400">
                <Calculator className="w-16 h-16 mx-auto mb-4 opacity-20" />
                <p className="text-lg font-medium text-slate-500">Ready to Calculate</p>
                <p className="text-sm mt-2 max-w-[200px] mx-auto opacity-75">Enter your weight and height details to see your health status.</p>
            </div>
          )}
      </div>
    </div>
  );
};

