export const ConfidenceInterval: React.FC = () => {
    const [mean, setMean] = useState(0);
    const [stdDev, setStdDev] = useState(1);
    const [size, setSize] = useState(10);
    const [confidence, setConfidence] = useState(0.95);
    const [result, setResult] = useState<{lower: number, upper: number} | null>(null);

    const calc = () => {
        // Z-score approx
        let z = 1.96;
        if(confidence === 0.90) z = 1.645;
        if(confidence === 0.99) z = 2.576;
        
        const margin = z * (stdDev / Math.sqrt(size));
        setResult({lower: mean - margin, upper: mean + margin});
    }

    return (
        <div className="grid md:grid-cols-2 gap-8 max-w-4xl mx-auto">
            <div className="space-y-4 bg-white p-6 rounded-xl border border-slate-200 shadow-sm">
                <div><label className="block text-xs font-bold uppercase text-slate-500 mb-1">Sample Mean (x̄)</label><input type="number" value={mean} onChange={e=>setMean(+e.target.value)} className="w-full p-2 border border-slate-300 rounded-lg focus:ring-2 focus:ring-brand-500 outline-none"/></div>
                <div><label className="block text-xs font-bold uppercase text-slate-500 mb-1">Standard Deviation (s)</label><input type="number" value={stdDev} onChange={e=>setStdDev(+e.target.value)} className="w-full p-2 border border-slate-300 rounded-lg focus:ring-2 focus:ring-brand-500 outline-none"/></div>
                <div><label className="block text-xs font-bold uppercase text-slate-500 mb-1">Sample Size (n)</label><input type="number" value={size} onChange={e=>setSize(+e.target.value)} className="w-full p-2 border border-slate-300 rounded-lg focus:ring-2 focus:ring-brand-500 outline-none"/></div>
                <div>
                    <label className="block text-xs font-bold uppercase text-slate-500 mb-1">Confidence Level</label>
                    <select value={confidence} onChange={e=>setConfidence(+e.target.value)} className="w-full p-2 border border-slate-300 rounded-lg bg-white focus:ring-2 focus:ring-brand-500 outline-none">
                        <option value={0.90}>90%</option>
                        <option value={0.95}>95%</option>
                        <option value={0.99}>99%</option>
                    </select>
                </div>
                <button onClick={calc} className="w-full bg-brand-600 text-white py-3 rounded-lg font-bold hover:bg-brand-700 mt-2 transition">Calculate Interval</button>
            </div>
            <div className="flex items-center justify-center bg-slate-50 rounded-xl p-6 border border-slate-200">
                {result ? (
                    <div className="text-center">
                        <div className="text-sm text-slate-500 mb-2 font-medium uppercase tracking-wider">Confidence Interval</div>
                        <div className="text-3xl font-bold text-slate-800 mb-1">[{result.lower.toFixed(4)}, {result.upper.toFixed(4)}]</div>
                        <div className="text-sm text-slate-400">Margin of Error: ±{((result.upper - result.lower)/2).toFixed(4)}</div>
                    </div>
                ) : (
                    <div className="text-slate-400 text-center">
                        <p>Enter sample data to calculate the confidence interval.</p>
                    </div>
                )}
            </div>
        </div>
    )
}
